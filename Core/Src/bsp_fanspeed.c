void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{	
	osStatus_t os_Status;
	
	/*为了传输方便，建立一个结构体传给消息队列数据*/
	GetFANOrg_Frequency getorg_fanfreq = {0};
	GetFANOrg_duty_cycle getorg_dutycycle = {0};
	if(htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1)//注意这里为HAL_TIM_ACTIVE_CHANNEL_1而不是TIM_CHANNEL_1
	{			
		/*数据融合准备入队*/
		getorg_fanfreq.org_rk_pwm_risingcount 	= HAL_TIM_ReadCapturedValue(&htim4 , TIM_CHANNEL_1);			
		getorg_fanfreq.org_arm_pwm_risingcount = HAL_TIM_ReadCapturedValue(&htim2 , TIM_CHANNEL_1);
		getorg_fanfreq.org_big_pwm_risingcount = HAL_TIM_ReadCapturedValue(&htim3 , TIM_CHANNEL_1);	
		//入队
		os_Status = osMessageQueuePut(msgQueue_GetFanFrequency_Data,  	/* 消息队列的名字 */
									  &getorg_fanfreq, 				/* 放入消息队列的变量（消息） */
									  NULL,      						/* 放入消息的优先级 */
									  NULL      	 					/* 超时时间为0，如果osMessageQueuePut被中断回调函数调用时，此参数必须是NULL */
									  );			 
	}
	if(htim->Channel == HAL_TIM_ACTIVE_CHANNEL_2)
	{		
		getorg_dutycycle.org_rk_pwm_fallingcount 	= HAL_TIM_ReadCapturedValue(&htim4 , TIM_CHANNEL_2);
		getorg_dutycycle.org_arm_pwm_fallingcount 	= HAL_TIM_ReadCapturedValue(&htim2 , TIM_CHANNEL_2);
		getorg_dutycycle.org_big_pwm_fallingcount 	= HAL_TIM_ReadCapturedValue(&htim3 , TIM_CHANNEL_2);
		
		/*融合准备入队*/
		//入队
		os_Status = osMessageQueuePut(msgQueue_GetFanDuty_cycle_Data,  	/* 消息队列的名字 */
									  &getorg_dutycycle, 				/* 放入消息队列的变量（消息） */
									  NULL,      						/* 放入消息的优先级 */
									  NULL      	 					/* 超时时间为0，如果osMessageQueuePut被中断回调函数调用时，此参数必须是NULL */
									  );
	}	
}


